<script>
document.addEventListener('DOMContentLoaded', function() {
    // Chatbot elements
    const chatbotToggle = document.getElementById('chatbotToggle');
    const chatbotContainer = document.getElementById('chatbotContainer');
    const chatbotClose = document.getElementById('chatbotClose');
    const chatbotBody = document.getElementById('chatbotBody');
    const chatbotInput = document.getElementById('chatbotInput');
    const chatbotSend = document.getElementById('chatbotSend');
    const chatMessages = document.getElementById('chatMessages');
    const typingIndicator = document.getElementById('typingIndicator');
    const suggestionsContainer = document.getElementById('suggestionsContainer');
    const noSuggestionsMessage = document.getElementById('noSuggestionsMessage');

    let isChatbotOpen = false;
    let suggestionsLoaded = false;

    // Toggle chatbot
    chatbotToggle.addEventListener('click', function() {
        if (!isChatbotOpen) {
            openChatbot();
        } else {
            closeChatbot();
        }
    });

    chatbotClose.addEventListener('click', closeChatbot);

    // Send message on button click
    chatbotSend.addEventListener('click', sendMessage);

    // Send message on Enter key
    chatbotInput.addEventListener('keypress', function(e) {
        if (e.key === 'Enter') {
            sendMessage();
        }
    });

    function openChatbot() {
        chatbotContainer.classList.remove('d-none');
        isChatbotOpen = true;
        if (!suggestionsLoaded) {
            loadSuggestions();
        }
        chatbotInput.focus();
    }

    function closeChatbot() {
        chatbotContainer.classList.add('d-none');
        isChatbotOpen = false;
    }

    function loadSuggestions() {
        // Clear previous suggestions
        suggestionsContainer.innerHTML = '';
        noSuggestionsMessage.classList.add('d-none');

        // Fetch suggestions from server
        fetch('chatbot_get_suggestions.php')
            .then(response => {
                if (!response.ok) {
                    throw new Error('Network response was not ok');
                }
                return response.json();
            })
            .then(data => {
                if (data.success && data.suggestions && data.suggestions.length > 0) {
                    data.suggestions.forEach(suggestion => {
                        const suggestionButton = document.createElement('button');
                        suggestionButton.type = 'button';
                        suggestionButton.className = 'quick-question btn btn-outline-primary btn-sm';
                        suggestionButton.textContent = suggestion.text;
                        suggestionButton.addEventListener('click', function() {
                            // Use the first keyword as the question to send
                            const question = suggestion.keywords && suggestion.keywords.length > 0 
                                ? suggestion.keywords[0] 
                                : suggestion.text;
                            addMessage(question, 'user');
                            simulateResponse(suggestion.response);
                        });
                        suggestionsContainer.appendChild(suggestionButton);
                    });
                    suggestionsLoaded = true;
                } else {
                    noSuggestionsMessage.classList.remove('d-none');
                }
            })
            .catch(error => {
                console.error('Error loading suggestions:', error);
                noSuggestionsMessage.classList.remove('d-none');
            });
    }

    function sendMessage() {
        const message = chatbotInput.value.trim();
        if (message === '') return;

        addMessage(message, 'user');
        chatbotInput.value = '';

        // Show typing indicator
        showTypingIndicator();

        // Send message to chatbot
        fetch('chatbot_process.php', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/x-www-form-urlencoded',
            },
            body: 'message=' + encodeURIComponent(message)
        })
        .then(response => {
            if (!response.ok) {
                throw new Error('Network response was not ok');
            }
            return response.json();
        })
        .then(data => {
            hideTypingIndicator();
            if (data.success) {
                addMessage(data.response, 'bot');
                
                // Check if this is a "not found" response - ALWAYS show suggestions for not found
                if (data.response.includes("I'm sorry, I don't have an answer") || 
                    data.response.includes("don't have an answer") ||
                    data.response.includes("contact the school administration")) {
                    // Suggestions are always visible, no need to hide them
                }
            } else {
                addMessage('Sorry, I encountered an error. Please try again.', 'bot');
            }
        })
        .catch(error => {
            hideTypingIndicator();
            console.error('Error:', error);
            addMessage('Sorry, I\'m having trouble connecting. Please try again later.', 'bot');
        });
    }

    function simulateResponse(response) {
        showTypingIndicator();
        setTimeout(() => {
            hideTypingIndicator();
            addMessage(response, 'bot');
        }, 1000);
    }

    function addMessage(text, sender) {
        const messageDiv = document.createElement('div');
        messageDiv.className = `mb-3 d-flex ${sender === 'user' ? 'justify-content-end' : 'justify-content-start'}`;
        
        const messageBubble = document.createElement('div');
        messageBubble.className = `rounded p-3 chatbot-message ${
            sender === 'user' 
                ? 'bg-primary text-white' 
                : 'bg-light text-dark'
        }`;
        messageBubble.textContent = text;
        
        messageDiv.appendChild(messageBubble);
        chatMessages.appendChild(messageDiv);
        
        // Scroll to bottom
        chatbotBody.scrollTop = chatbotBody.scrollHeight;
    }

    function showTypingIndicator() {
        typingIndicator.classList.remove('d-none');
        chatbotBody.scrollTop = chatbotBody.scrollHeight;
    }

    function hideTypingIndicator() {
        typingIndicator.classList.add('d-none');
    }

    // Close chatbot when clicking outside
    document.addEventListener('click', function(event) {
        if (isChatbotOpen && 
            !chatbotContainer.contains(event.target) && 
            !chatbotToggle.contains(event.target)) {
            closeChatbot();
        }
    });
});
</script>

